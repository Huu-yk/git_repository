     */
    public function index()
    {
        $per = array();
        $userid = Auth::id();
        $roleid = DB::table('role_user')->select('role_id')->where(['user_id'=>$userid])->get();
        foreach($roleid as $id){
            $permissionid = DB::table('permission_role')->select('permission_id')->where(['role_id'=>$id->role_id])->get();
            foreach($permissionid as $pid){
                $permissionurl = DB::table('permissions')->select('url')->where(['id'=>$pid->permission_id])->get();
                $per[] = $permissionurl[0];
            }
        }
        return $per;
    }

